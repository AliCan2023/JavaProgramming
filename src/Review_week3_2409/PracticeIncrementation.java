package Review_week3_2409;

public class PracticeIncrementation {
    public static void main(String[] args) {

        int x = 10;
        int number = x++ + 100;
        System.out.println("number = " + number);

        int number2 = ++x + 100;

        System.out.println("number2 = " + number2);

    }
}
